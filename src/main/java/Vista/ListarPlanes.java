    private void planesExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_planesExcelActionPerformed
        try {
            Workbook wb = new XSSFWorkbook();
            Sheet sheet = wb.createSheet("Hoja 1");
            Row rowCol = sheet.createRow(0);
            for (int i = 0; i < listarPlanes.getColumnCount(); i++) {
                Cell cell = rowCol.createCell(i);
                cell.setCellValue(listarPlanes.getColumnName(i));
            }
            for (int i = 0; i < listarPlanes.getRowCount(); i++) {
                Row row = sheet.createRow(i + 1);
                for (int j = 0; j < listarPlanes.getColumnCount(); j++) {
                    Cell cell = row.createCell(j);
                    if (listarPlanes.getValueAt(i, j) != null) {
                        cell.setCellValue(listarPlanes.getValueAt(i, j).toString());
                    }
                }
            }
            String nombre = JOptionPane.showInputDialog("Nombre del archivo: ");
            FileOutputStream out = new FileOutputStream(new File(nombre+".xlsx"));
            wb.write(out);
            wb.close();
            out.close();
            System.out.println(out);
        } catch(FileNotFoundException e){
            System.out.println(e);
        }catch(IOException io){
            System.out.println(io);
        }
    }//GEN-LAST:event_planesExcelActionPerformed
 
